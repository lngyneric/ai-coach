def export_to_html(export_data: dict) -> str:
    """Export shifu as a standalone HTML document."""
    shifu = export_data.get("shifu", {})
    items = _parse_outline_tree(
        export_data.get("outline_items", []),
        export_data.get("structure", {}),
    )

    body_parts = []
    body_parts.append(f'<h1>{html_mod.escape(shifu.get("title", "Untitled"))}</h1>')
    if shifu.get("description"):
        body_parts.append(
            f'<blockquote>{html_mod.escape(shifu.get("description", ""))}</blockquote>'
        )
    body_parts.append(
        f'<p class="meta">📅 {export_data.get("exported_at", "")}</p>'
    )
    body_parts.append("<hr/>")

    for item in items:
        depth = item.get("_depth", 1)
        tag = f"h{min(depth + 1, 6)}"
        body_parts.append(
            f'<{tag} id="{html_mod.escape(item.get("outline_item_bid", ""))}">'
            f'{html_mod.escape(item.get("title", ""))}'
            f"</{tag}>"
        )
        content = _render_mdflow_to_html(item.get("content", ""))
        if content:
            body_parts.append(content)

    return HTML_TEMPLATE.format(
        title=html_mod.escape(shifu.get("title", "Untitled")),
        body="\n".join(body_parts),
        exported_at=export_data.get("exported_at", ""),
    )
