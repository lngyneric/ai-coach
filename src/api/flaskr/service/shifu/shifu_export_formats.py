# ── Markdown export ──

def export_to_markdown(export_data: dict) -> str:
    """Export shifu as a Markdown document."""
    shifu = export_data.get("shifu", {})
    items = _parse_outline_tree(
        export_data.get("outline_items", []),
        export_data.get("structure", {}),
    )

    md = []
    md.append(f"# {shifu.get('title', 'Untitled')}\n")
    if shifu.get("description"):
        md.append(f"> {shifu.get('description')}\n")
    md.append(f"")
    md.append(f"_Exported at: {export_data.get('exported_at', '')}_\n")
    md.append("---\n")

    for item in items:
        depth = item.get("_depth", 1)
        prefix = "#" * min(depth + 1, 6)
        md.append(f"\n{prefix} {item.get('title', '')}\n")
        content = _mdflow_to_text(item.get("content", ""))
        if content:
            md.append(f"{content}\n")

    md.append("\n---\n")
    md.append(f"_Generated by AI-Shifu v{export_data.get('version', '1.0')}_\n")
    return "\n".join(md)


