"""
Shifu export formats — Markdown, HTML, and more.

Depends on the JSON export data structure from shifu_import_export_funcs.
"""

import re
import html as html_mod
from datetime import datetime
from typing import Any


def _parse_outline_tree(items: list[dict], structure: dict) -> list[dict]:
    """Attach outline items to their positions in the tree structure."""
    item_map = {i["outline_item_bid"]: i for i in items}
    enriched = []

    def walk(node: dict, depth: int):
        if node.get("type") == "outline":
            bid = node.get("id", "")
            item = item_map.get(bid)
            if item and not item.get("hidden"):
                enriched.append({**item, "_depth": depth})
        for child in node.get("children", []):
            walk(child, depth + 1)

    for root in structure.get("children", []):
        walk(root, 1)

    return enriched


def _mdflow_to_text(content: str) -> str:
    """Strip MDFlow markers but keep text, images, links."""
    if not content:
        return ""
    # Remove MDFlow section markers
    text = re.sub(r"^===\s*#+?\s*", "### ", content, flags=re.MULTILINE)
    text = re.sub(r"^===\s*", "", text, flags=re.MULTILINE)
    # Remove HTML comments
    text = re.sub(r"<!--.*?-->", "", text, flags=re.DOTALL)
    return text.strip()


def _render_mdflow_to_html(content: str) -> str:
    """Convert MDFlow content to HTML."""
    if not content:
        return ""
    html_parts = []
    lines = content.split("\n")
    in_section = False

    for line in lines:
        stripped = line.strip()
        # Section marker: === # Title
        m = re.match(r"^===\s*#+\s*(.+)$", stripped)
        if m:
            if in_section:
                html_parts.append("</div>")
            html_parts.append(f'<div class="mdf-section">')
            html_parts.append(f"<h3>{html_mod.escape(m.group(1))}</h3>")
            in_section = True
            continue
        if stripped == "---":
            continue
        # Images: ![alt](url)
        img_match = re.match(r"^!\[(.*?)\]\((.*?)\)$", stripped)
        if img_match:
            html_parts.append(
                f'<figure><img src="{html_mod.escape(img_match.group(2))}" '
                f'alt="{html_mod.escape(img_match.group(1))}" '
                f'style="max-width:100%;height:auto;border-radius:8px;" />'
                f"</figure>"
            )
            continue
        # Videos: detected by URL extensions
        video_match = re.search(
            r'\((https?://[^)]+\.(mp4|m3u8|webm)[^)]*)\)', stripped
        )
        if video_match:
            url = video_match.group(1)
            html_parts.append(
                f'<video controls style="max-width:100%;border-radius:8px;">'
                f'<source src="{html_mod.escape(url)}">'
                f"</video>"
            )
            continue
        # Regular paragraphs
        if stripped:
            html_parts.append(f"<p>{html_mod.escape(stripped)}</p>")
        else:
            html_parts.append("<br/>")

    if in_section:
        html_parts.append("</div>")
    return "\n".join(html_parts)


